import os
import shutil
import subprocess
import sys

APP_ENTRYPOINT = "run.py"


def find_python_base_command():
    py_launcher = shutil.which("py")
    if py_launcher:
        return [py_launcher, "-3"]

    python_cmd = shutil.which("python")
    if python_cmd:
        return [python_cmd]

    return None


def resolve_project_dir():
    start_dir = os.path.dirname(os.path.abspath(sys.executable if getattr(sys, "frozen", False) else __file__))
    candidates = [start_dir, os.path.dirname(start_dir)]

    for candidate in candidates:
        run_path = os.path.join(candidate, APP_ENTRYPOINT)
        if os.path.exists(run_path):
            return candidate

    return start_dir


def main():
    project_dir = resolve_project_dir()
    run_path = os.path.join(project_dir, APP_ENTRYPOINT)

    if not os.path.exists(run_path):
        print("run.py not found.")
        print("Place this EXE in the project folder or in project/dist.")
        return 1

    python_base = find_python_base_command()
    if python_base is None:
        print("No Python installation found in PATH.")
        print("Install Python 3.10+ and enable 'Add Python to PATH' or py launcher.")
        return 1

    command = python_base + [run_path]
    return subprocess.call(command, cwd=project_dir)


if __name__ == "__main__":
    raise SystemExit(main())
